import mongoose from "mongoose";

const recyclingCollectionSchema = mongoose.Schema({
  locationName: {
    type: String,
    required: true,
  },
  address: {
    street: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    postalCode: {
      type: String,
      required: true,
    },
    state: {
      type: String,
      required: true,
    },
    country: {
      type: String,
      required: true,
    },
  },
  contactNumber: {
    type: String,
    required: true,
  },
  latitude: {
    type: Number,
    required: true,
  },
  longitude: {
    type: Number,
    required: true,
  },
  
});

const RecyclingCollection = mongoose.model("RecyclingCollection", recyclingCollectionSchema);

export default RecyclingCollection;